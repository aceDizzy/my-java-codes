package sourceCodes;

public class PairedArraysByIndex {
	  public static void main(String[] args) {

	    // 3 arrays
	    String email[] = {"ace@yahoo.com","acdc@yahoo.com","adis@yahoo.com"};
	    String user[] = {"Nairobi", "Tokyo", "Rio"};
	    String pw[] = {"ace001","acdc002","adis003"};

	    int i = 0;

	    System.out.println("Index    : " + i);
	    System.out.println("Email    : " + email[i]);
	    System.out.println("Username : " + user[i]);
	    System.out.println("Password : " + pw[i]);


	  }
	}